public class operators{
    public static void main(String args[]){
        // AND
        //   5-> 101
        // & 6-> 110
        // ----------
        //       100
        System.out.println(5&6);


        // OR
        //   5-> 101
        // | 6-> 110
        // ----------
        //       111
        System.out.println(5|6);


        // XOR
        //   5-> 101
        // ^ 6-> 110
        // ----------
        //       011
        System.out.println(5^6);
    }
}